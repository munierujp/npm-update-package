import assert from 'node:assert'
import {
  describe,
  it
} from 'node:test'
import pkg from '../../../../package.json'
import { createFooter } from './createFooter.js'

await describe('createFooter', async () => {
  await it('returns footer', () => {
    const actual = createFooter()

    assert.strictEqual(actual, `This PR has been generated by [${pkg.name}](${pkg.homepage}) v${pkg.version}`)
  })
})
